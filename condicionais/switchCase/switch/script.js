//estrutura de decisão
//switch/case /break/default


let fruta = "banana"


switch(fruta){
    case "laranja":
        console.log("suco laranja")
        break

        case "banana":
        console.log("vitamina banana")
        break

        case "maça" :
            console.log("suco maça")
            break

        
        default:
            console.log("suco generico")
}